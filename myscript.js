	var price2=document.getElementById("priceM2");
	var item3=document.getElementById("itemM3");
	var price3=document.getElementById("priceM3");
	var item4=document.getElementById("itemM4");
	var price4=document.getElementById("priceM4");
	item1.innerHTML=item2.innerHTML;
	price1.innerHTML=price2.innerHTML;
	item2.innerHTML=item3.innerHTML;
	price2.innerHTML=price3.innerHTML;
